#include "gdef.h"
#include "gnet.h"
#include "glinebuffer.h"
#include "gdebug.h"
#include "gassert.h"

unsigned long GNet::LineBuffer::m_limit = 1000000UL ; // 1Mb denial-of-service line-length limit

GNet::LineBuffer::LineBuffer( const std::string & eol , bool do_throw ) :
	m_eol(eol) ,
	m_more(false) ,
	m_throw(do_throw)
{
	G_ASSERT( !eol.empty() ) ;
}

void GNet::LineBuffer::add( const char * p , std::string::size_type n )
{
	G_ASSERT( p != NULL ) ;
	check( n ) ;
	m_store.append( p , n ) ;
	load() ;
}

void GNet::LineBuffer::add( const std::string & s )
{
	check( s.length() ) ;
	m_store.append( s ) ;
	load() ;
}

void GNet::LineBuffer::check( std::string::size_type n )
{
	if( (m_store.length()+n) > m_limit )
	{
		std::string::size_type total = m_store.size() + m_current.size() + n ;
		m_store.erase() ;
		m_current.erase() ;
		m_more = false ;
		if( m_throw )
			throw Overflow() ;
		else
			G_ERROR( "GNet::LineBuffer::check: line too long: discarding " << total << " bytes" ) ;
	}
}

void GNet::LineBuffer::load()
{
	if( ! m_more )
	{
		std::string::size_type pos = m_store.find(m_eol) ; // optimisation opportunity here using find(pos,s) overload
		if( pos != std::string::npos )
		{
			m_more = true ;
			m_current = m_store.substr(0U,pos) ;
			m_store.erase( 0U , pos + m_eol.length() ) ;
		}
	}
}

bool GNet::LineBuffer::more() const
{
	return m_more ;
}

const std::string & GNet::LineBuffer::current() const
{
	return m_current ;
}

void GNet::LineBuffer::discard()
{
	m_current.erase() ;
	m_more = false ;
	load() ;
}

std::string GNet::LineBuffer::line()
{
	std::string s( current() ) ;
	discard() ;
	return s ;
}

