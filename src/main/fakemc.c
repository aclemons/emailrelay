//
// fakemc.c
//
// A nasty little utility to create the files normally
// generated by microsoft's "mc" message compiler. This
// is intended to be used when the Windows development
// environment (eg. MinGW) does not come with a message 
// compiler of its own.
//
// usage: fakemc [<messages_mc_path>]
//
// The output files are put into the same directory as
// the given file, but otherwise the command-line 
// parameter is not used.
//

#include <stdio.h>
#include <string.h>
#include <stdlib.h>

static char * dirname( const char * path )
{
	char * dir  = strdup( path ) ; // leak
	char * p = strrchr( dir , '\\' ) ;
	if( p ) *p = '\0' ;
	return p ? dir : "." ;
}

static char * join( const char * dir , const char * filename )
{
	char * p = malloc( strlen(dir) + strlen(filename) + 2U ) ; // leak
	if( p == NULL ) abort() ;
	strcpy( p , dir ) ;
	strcat( p , "\\" ) ;
	strcat( p , filename ) ;
	return p ;
}

int main( int argc , char * argv [] )
{
	// data for message ids 1001, 1002, 1003 (info, warning and error respectively) generated on Windows7
	static unsigned int i , bin[] = { 
		0x03, 0x00, 0x00, 0x00, 0xe9, 0x03, 0x00, 0x40, 0xe9, 0x03, 0x00, 0x40, 0x28, 0x00, 0x00, 0x00,
		0xea, 0x03, 0x00, 0x80, 0xea, 0x03, 0x00, 0x80, 0x3c, 0x00, 0x00, 0x00, 0xeb, 0x03, 0x00, 0xc0,
		0xeb, 0x03, 0x00, 0xc0, 0x50, 0x00, 0x00, 0x00, 0x14, 0x00, 0x01, 0x00, 0x25, 0x00, 0x31, 0x00,
		0x21, 0x00, 0x73, 0x00, 0x21, 0x00, 0x0d, 0x00, 0x0a, 0x00, 0x00, 0x00, 0x14, 0x00, 0x01, 0x00,
		0x25, 0x00, 0x31, 0x00, 0x21, 0x00, 0x73, 0x00, 0x21, 0x00, 0x0d, 0x00, 0x0a, 0x00, 0x00, 0x00,
		0x14, 0x00, 0x01, 0x00, 0x25, 0x00, 0x31, 0x00, 0x21, 0x00, 0x73, 0x00, 0x21, 0x00, 0x0d, 0x00,
		0x0a, 0x00, 0x00, 0x00 } ;

	char * dir = dirname( argc > 1 ? argv[1] : ".\\messages.mc" ) ;
	FILE * fp_rc = fopen( join(dir,"messages.rc") , "wt" ) ;
	FILE * fp_bin = fopen( join(dir,"MSG00001.bin") , "wb" ) ;
	if( !fp_rc || !fp_bin ) 
	{ 
		fprintf( stderr , "%s: failed\n" , argv[0] ) ; 
		return 1 ; 
	}
	for( i = 0 ; i < (sizeof(bin)/sizeof(bin[0])) ; i++ ) 
	{
		fprintf( fp_bin , "%c" , (char)(bin[i]) ) ;
	}
	fprintf( fp_rc , "%s\n" , "LANGUAGE 0x9,0x1" ) ;
	fprintf( fp_rc , "%s\n" , "1 11 \"MSG00001.bin\"" ) ;
	fclose( fp_rc ) ;
	fclose( fp_bin ) ;
	return 0 ;
}

